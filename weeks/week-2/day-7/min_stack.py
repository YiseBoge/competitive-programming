class MinStack:

    def __init__(self):
        """
        initialize your data structure here.
        """
        self.stack = []

    def push(self, x: int) -> None:
        self.stack.append(x)

    def pop(self) -> None:
        self.stack.pop()

    def top(self) -> int:
        val = self.stack.pop()
        self.push(val)
        return val

    def getMin(self) -> int:
        return min(self.stack)
